package edu.fsu.cs.mobile.studybuddy;

public class UserInfo {
    private String Name;
    private String active;

    public UserInfo(){

    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getActive() {
        return active;
    }

    public void setActive(String active) {
        this.active = active;
    }
}
